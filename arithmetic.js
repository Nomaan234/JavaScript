let a=prompt("enter value of a");
let b=prompt("enter value of b");

a = parseFloat(a);   
b = parseFloat(b);

console.log(" a+b =", a + b );
console.log(" a-b =", a - b );
console.log(" a*b =", a * b );
console.log(" a/b =", a / b );
console.log(" a%b =", a % b );
console.log(" a**b =", a ** b );
console.log(" a++ =", ++a );
console.log(" b++ =", ++b );
